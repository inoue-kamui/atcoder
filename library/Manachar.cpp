vector<int> manacher(const string& s) {
    int n = s.size();
    vector<int> R(n);
    int i = 0, j = 0;
    while (i < n) {
        while (i - j >= 0 && i + j < n && s[i - j] == s[i + j]) ++j;
        R[i] = j;
        int k = 1;
        while (i - k >= 0 && k + R[i - k] < j) {
            R[i + k] = R[i - k];
            ++k;
        }
        i += k;j -= k;
    }
    return R;
}
// 奇数長の回文しか検出できないので注意。偶数長に対応させるためには、文字列の間に#を挿入するなどして奇数長の回文に帰着する必要あり
// そして、#を中心とする回文の長さを求めるときは、R[i] - 1 がそのまま回文の長さになる
